fb::gl::EnumHelper::EnumHelper() {

    init_table();

}

const std::string& fb::gl::EnumHelper::pretty_print_enum(GLenum e) {

    static const std::string unknown_str = "<unknown GLenum>";

    // This will lazy-initialize the table.
    const EnumHelper& helper = get();

    const auto itr = helper.table_.find(e);

    if (itr != std::end(helper.table_)) {

        return itr->second;

    } else {

        FB_LOG_ERROR << "Encountered unknown GL enum '" << std::hex << e << "'.";

        return unknown_str;

    }

}

void fb::gl::EnumHelper::lookup_insert(uint64_t e, std::string&& val) {

    auto itr = table_.find(e);
    if (itr != std::end(table_)) {

        itr->second.append(" aka " + val);

    } else {
        std::ostringstream oss;
        oss << val << " (" << std::hex << e << ")";
        auto result = table_.insert(std::make_pair(e, oss.str()));
        FB_ASSERT(result.second);
    }

}

